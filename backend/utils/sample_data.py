# Function to get all sample questions
def get_all_sample_questions():
    return SAMPLE_MCQ_QUESTIONS + SAMPLE_SUBJECTIVE_QUESTIONS + SAMPLE_ESSAY_TOPICS

# Function to get questions by type
def get_questions_by_type(q_type: str):
    if q_type == "mcq":
        return SAMPLE_MCQ_QUESTIONS
    elif q_type == "subjective":
        return SAMPLE_SUBJECTIVE_QUESTIONS
    elif q_type == "essay":
        return SAMPLE_ESSAY_TOPICS
    return []

# Function to get questions by subject
def get_questions_by_subject(subject: str):
    all_questions = get_all_sample_questions()
    return [q for q in all_questions if q["subject"].lower() == subject.lower()]

# Function to get questions by year
def get_questions_by_year(year: int):
    all_questions = get_all_sample_questions()
    return [q for q in all_questions if q.get("year") == year]
